create table menu_food
(
    menu_id int not null comment '对应菜谱id',
    food_id int not null comment '对应食材id',
    primary key (menu_id, food_id),
    constraint menu_food___fk1
        foreign key (menu_id) references menu (id)
            on update cascade on delete cascade,
    constraint menu_food___fk2
        foreign key (food_id) references food_warehouse (id)
            on update cascade on delete cascade
)
    comment '菜谱和食材的关联表';

create index menu_food_food_warehouse_id_fk
    on menu_food (food_id);

